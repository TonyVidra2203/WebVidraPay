# -----------------------------------------------------------------------------
# Раздел: Импорты
# -----------------------------------------------------------------------------
from __future__ import annotations

from aiogram import types
from aiogram.types import InlineKeyboardButton, InlineKeyboardMarkup
from keyboards.inline import Callback


# -----------------------------------------------------------------------------
# Раздел: Константы
# -----------------------------------------------------------------------------
RULES_TEXT: str = (
    "1. Обменник нельзя использовать для любых незаконных или мошеннических переводов. "
    "При возникновении подозрений администрация вправе запросить у вас документы, удостоверяющие личность.\n\n"
    "2. Администрация оставляет за собой право отказать в выполнении заявки без объяснений – это касается любого клиента.\n\n"
    "3. Запрещено: отправка через номер 900, использование банкоматов/терминалов и разбиение платежа на несколько частей без согласования.\n\n"
    "4. Для защиты от мошенничества применяется внутренняя система анализа транзакций и поведения пользователей.\n\n"
    "5. Обмен проводится через сторонние сервисы. Администрация не несёт ответственности за блокировки биржевых аккаунтов; "
    "рекомендуем использовать кошельки Electrum, Exodus, Ledger, Trust Wallet и подобные.\n\n"
    "6. Запрещено напрямую связываться с получателем средств. Нарушители будут автоматически заблокированы."
)


# -----------------------------------------------------------------------------
# Раздел: Асинхронные функции
# -----------------------------------------------------------------------------
async def show_rules(callback: types.CallbackQuery) -> None:
    """Показать правила: скрыть старое меню и отправить новый текст с кнопкой."""
    await callback.answer()

    try:
        await callback.message.delete()
    except Exception:
        pass

    kb = InlineKeyboardMarkup(row_width=1).add(
        InlineKeyboardButton(text="Главное меню", callback_data=Callback.CANCEL_BUY)
    )

    await callback.bot.send_message(
        chat_id=callback.from_user.id,
        text=RULES_TEXT,
        parse_mode="HTML",
        reply_markup=kb,
    )
